package xavier.ricardo.softws.tipos;

public class Item {

	private int seq;
	private String codProduto;
	private String subCodigo;
	private int qtde;
	private String medidas;
	private double preco;
	private String descricao;
	private String texto;
	private String codEspecificos;
	public int getSeq() {
		return seq;
	}
	public void setSeq(int seq) {
		this.seq = seq;
	}
	public String getCodProduto() {
		return codProduto;
	}
	public void setCodProduto(String codProduto) {
		this.codProduto = codProduto;
	}
	public String getSubCodigo() {
		return subCodigo;
	}
	public void setSubCodigo(String subCodigo) {
		this.subCodigo = subCodigo;
	}
	public int getQtde() {
		return qtde;
	}
	public void setQtde(int qtde) {
		this.qtde = qtde;
	}
	public String getMedidas() {
		return medidas;
	}
	public void setMedidas(String medidas) {
		this.medidas = medidas;
	}
	public double getPreco() {
		return preco;
	}
	public void setPreco(double preco) {
		this.preco = preco;
	}
	public String getDescricao() {
		return descricao;
	}
	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}
	public String getTexto() {
		return texto;
	}
	public void setTexto(String texto) {
		this.texto = texto;
	}
	public String getCodEspecificos() {
		return codEspecificos;
	}
	public void setCodEspecificos(String codEspecificos) {
		this.codEspecificos = codEspecificos;
	}
	
	
}
